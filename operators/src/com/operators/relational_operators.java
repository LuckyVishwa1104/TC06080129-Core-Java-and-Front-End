package com.operators;

public class relational_operators 
{

	public static void main(String[] args) 
	{
		// relational operators - they are used for comparing values
		// it return either true or false
		int a=10,b=20;
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>=b);
	}

}
